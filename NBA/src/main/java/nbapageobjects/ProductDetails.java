package nbapageobjects;

public class ProductDetails {
private String title;
private String price;
private String topSellerMessage;
public String getTitle() {
	return title;
}
public void setTitle(String title) {
	this.title = title;
}
public String getPrice() {
	return price;
}
public void setPrice(String price) {
	this.price = price;
}
public String getTopSellerMessage() {
	return topSellerMessage;
}
public void setTopSellerMessage(String topSellerMessage) {
	this.topSellerMessage = topSellerMessage;
}
}
